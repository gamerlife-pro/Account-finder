// Helper to generate a dummy 7x24 heatmap (7 rows of days, 24 columns of hours)
function generateMockHeatmap(seed: number): number[][] {
  const heatmap: number[][] = [];
  for (let d = 0; d < 7; d++) {
    const row: number[] = [];
    for (let h = 0; h < 24; h++) {
      // Create some realistic wave pattern for high-activity times (mid-day & evening)
      const baseActivity = Math.sin((h - 8) / 3) * 3 + Math.cos((d - 3) / 2) * 2 + (seed % 5);
      const scaledVal = Math.max(0, Math.min(10, Math.floor(baseActivity + Math.random() * 4)));
      row.push(scaledVal);
    }
    heatmap.push(row);
  }
  return heatmap;
}

// Generate follower history helper
function generateFollowerHistory(base: number, growthPct: number): { date: string; count: number }[] {
  const points = [];
  const months = ["Jan", "Feb", "Mar", "Apr", "May", "Jun"];
  let count = Math.floor(base * (1 - growthPct / 100));
  const increasePerMonth = Math.floor((base - count) / 5);

  for (let i = 0; i < 6; i++) {
    points.push({
      date: months[i],
      count: count,
    });
    count += increasePerMonth + Math.floor(Math.random() * (increasePerMonth * 0.1));
  }
  // Max out on the actual current base
  points[5].count = base;
  return points;
}

